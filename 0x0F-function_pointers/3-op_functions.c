#include "3-calc.h"

/**
 * op_add - calcs sum of two ints
 * @a: 1st int 
 * @b: 2nd int
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calcs diff of two ints
 * @a: 1st int
 * @b: 2nd int
 * Return: difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calcs product of two ints
 * @a: 1st int
 * @b: 2nd int
 * Return: product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calcs division of two ints
 * @a: 1st int
 * @b: 2nd int
 * Return: quotient
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calcs remainder of division of two ints
 * @a: 1st int
 * @b: 2nd int
 * Return: remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
